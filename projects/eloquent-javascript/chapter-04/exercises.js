// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step=1) {
  var myArray = [];
  if (start === end){
    return [];
  }
  if (step < 0){
    for(var i = start; i >= end; i -= step){
      myArray.push(i);
    }
  } else {
    for (var i = start; i <= end; i += step){
      myArray.push(i);
    }
  }
  return myArray;
}

